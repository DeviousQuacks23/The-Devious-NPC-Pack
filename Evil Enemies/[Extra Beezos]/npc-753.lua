function sampleNPC.onTickEndNPC(v)
	--Don't act during time freeze
	if Defines.levelFreeze then return end
	
	local data = v.data
	
	--If despawned
	if v.despawnTimer <= 0 then
		--Reset our properties, if necessary
		data.initialized = false
		return
	end

	--Initialize
	if not data.initialized then
		--Initialize necessary data.
		data.initialized = true
		data.state = 0
		v.speedY = -8
		v.y = Section(v.section).boundary.bottom
	end

	--Depending on the NPC, these checks must be handled differently
	if v:mem(0x12C, FIELD_WORD) ~= 0   --Grabbed
	or v:mem(0x136, FIELD_BOOL)        --Thrown
	or v:mem(0x138, FIELD_WORD) > 0    --Contained within
	then
		data.state = 1
		return
	end
	
	if data.state == 0 then
		v.speedX = v.direction * NPC.config[v.id].speed * 0.5
		v.speedY = math.min(0, v.speedY + 0.08)
		if lunatime.tick() % 4 == 0 then
			local x, y, x2, y2 = v.x + 0.5 * v.width, v.y + 0.1 * v.height, v.x + 0.5 * v.width + 400 * v.direction, v.y + 0.9 * v.height
			if x2 < x then
				x, x2 = x2, x
			end
			for k,p in ipairs(Player.getIntersecting(x, y, x2, y2)) do
				if p.deathTimer == 0 then
					data.state = 1
					v.speedY = 0
					break
				end
			end

			if v.speedY == 0 then
				data.state = 1
			end
		end
	else
		v.speedX = v.direction * NPC.config[v.id].speed
	end
end

--Gotta return the library table!
